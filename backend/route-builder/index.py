def osrm_route(lat1, lon1, lat2, lon2):
    """Маршрутизация: Россия напрямую, зоны — через КПП + дорожный граф."""
    from_zone = get_zone_type(lat1, lon1)
    to_zone = get_zone_type(lat2, lon2)
    print(f"[route] from=({lat1},{lon1}) zone={from_zone}, to=({lat2},{lon2}) zone={to_zone}")

    if not from_zone and not to_zone:
        result = osrm_multi([(lat1, lon1), (lat2, lon2)])
        if result:
            print(f"[osrm] direct: {len(result[0])} pts, {result[1]:.0f} km")
            return result
        return None

    if from_zone and to_zone:
        pts, km, hrs = build_zone_polyline(lat1, lon1, lat2, lon2)
        print(f"[osrm] zone-to-zone: {km:.0f} km, {len(pts)} pts")
        return pts, km, hrs

    zone_lat = lat2 if to_zone else lat1
    zone_lon = lon2 if to_zone else lon1
    use_bridge = zone_lon < 37.5

    if use_bridge:
        bridge_krasnodar = ENTRY_POINTS_BRIDGE["krasnodar"]
        bridge_port = ENTRY_POINTS_BRIDGE["port_kavkaz"]
        bridge_kerch = BRIDGE_KERCH

        if to_zone:
            osrm_coords = [(lat1, lon1), bridge_krasnodar, bridge_port]
            osrm_result = osrm_multi(osrm_coords)
            if not osrm_result:
                return None
            wps_r, km_r, hrs_r = osrm_result
            zone_pts, zone_km, zone_hrs = build_zone_polyline(bridge_kerch[0], bridge_kerch[1], lat2, lon2)
            all_wps = wps_r + zone_pts
            total_km = km_r + 19.0 + zone_km
            total_hrs = hrs_r + 0.3 + zone_hrs
        else:
            zone_pts, zone_km, zone_hrs = build_zone_polyline(lat1, lon1, bridge_kerch[0], bridge_kerch[1])
            osrm_coords = [bridge_port, bridge_krasnodar, (lat2, lon2)]
            osrm_result = osrm_multi(osrm_coords)
            if not osrm_result:
                return None
            wps_r, km_r, hrs_r = osrm_result
            all_wps = zone_pts + wps_r
            total_km = zone_km + 19.0 + km_r
            total_hrs = zone_hrs + 0.3 + hrs_r
    else:
        entry = ENTRY_POINTS_DNR_LNR["veselo"]
        if to_zone:
            osrm_coords = [(lat1, lon1), entry]
            osrm_result = osrm_multi(osrm_coords)
            if not osrm_result:
                return None
            wps_r, km_r, hrs_r = osrm_result
            zone_pts, zone_km, zone_hrs = build_zone_polyline(entry[0], entry[1], lat2, lon2)
            all_wps = wps_r + zone_pts
            total_km = km_r + zone_km
            total_hrs = hrs_r + zone_hrs
        else:
            zone_pts, zone_km, zone_hrs = build_zone_polyline(lat1, lon1, entry[0], entry[1])
            osrm_coords = [entry, (lat2, lon2)]
            osrm_result = osrm_multi(osrm_coords)
            if not osrm_result:
                return None
            wps_r, km_r, hrs_r = osrm_result
            all_wps = zone_pts + wps_r
            total_km = zone_km + km_r
            total_hrs = zone_hrs + hrs_r

    print(f"[osrm] route: {len(all_wps)} pts, {total_km:.0f} km, {total_hrs:.1f} h")
    return all_wps, total_km, total_hrs


def yandex_route(lat1, lon1, lat2, lon2):
    """Маршрут через Яндекс Routes API v2."""
    api_key = os.environ.get("YANDEX_ROUTES_API_KEY", "")
    if not api_key:
        print("[yandex] no API key")
        return None
    url = (f"https://api.routing.yandex.net/v2/route"
           f"?waypoints={lat1},{lon1}|{lat2},{lon2}&mode=driving&apikey={api_key}")
    req = urllib.request.Request(url, headers={"User-Agent": "route-builder/1.0"})
    try:
        with urllib.request.urlopen(req, timeout=30) as r:
            data = json.loads(r.read())
            print(f"[yandex] ok, legs={len(data.get('route',{}).get('legs',[]))}")
            return data
    except Exception as e:
        print(f"[yandex] error: {e}")
        return None


def extract_polyline(route_data):
    points = []
    total_duration = 0.0
    total_length = 0.0
    for leg in route_data.get("route", {}).get("legs", []):
        for step in leg.get("steps", []):
            total_duration += step.get("duration", 0)
            total_length += step.get("length", 0)
            for pt in step.get("polyline", {}).get("points", []):
                points.append((pt[1], pt[0]))
    return points, total_length / 1000.0, total_duration / 3600.0


def split_km_by_zone(waypoints, total_km):
    h_normal = 0.0
    h_special = 0.0
    for i in range(len(waypoints) - 1):
        lat1, lon1 = waypoints[i]
        lat2, lon2 = waypoints[i + 1]
        seg = haversine(lat1, lon1, lat2, lon2)
        mid_lat = (lat1 + lat2) / 2
        mid_lon = (lon1 + lon2) / 2
        if is_in_special_zone(mid_lat, mid_lon):
            h_special += seg
        else:
            h_normal += seg
    h_total = h_normal + h_special
    if h_total == 0:
        return total_km, 0.0
    ratio_special = h_special / h_total
    km_special = total_km * ratio_special
    km_normal = total_km * (1 - ratio_special)
    return round(km_normal), round(km_special)


def find_zone_segments(waypoints):
    segments = []
    current_zone = None
    start_idx = 0
    for i in range(len(waypoints)):
        lat, lon = waypoints[i]
        zone = get_zone_name(lat, lon)
        if zone != current_zone:
            if current_zone is not None:
                segments.append({"start_idx": start_idx, "end_idx": i - 1, "zone": current_zone})
            current_zone = zone
            start_idx = i
    if current_zone is not None:
        segments.append({"start_idx": start_idx, "end_idx": len(waypoints) - 1, "zone": current_zone})
    return segments


def normalize_city(name):
    import re
    n = name.strip()
    parts = [p.strip() for p in n.split(',')]
    skip_words = ['россия', 'область', 'регион', 'район', 'республика', 'округ', 'край']
    city_part = None
    for i, part in enumerate(parts):
        pl = part.lower()
        if re.match(r'^(г|с|п|пгт)\.?\s+', pl) or pl.startswith('город ') or pl.startswith('село '):
            city_part = part.strip()
            break
        if not any(w in pl for w in skip_words):
            city_part = part.strip()
    if city_part:
        n = city_part
    for prefix in ['г. ', 'г.', 'город ', 'с. ', 'с.', 'село ', 'п. ', 'п.', 'пгт. ', 'пгт.', 'пгт ']:
        if n.lower().startswith(prefix):
            n = n[len(prefix):].strip()
            break
    return n.strip().title()



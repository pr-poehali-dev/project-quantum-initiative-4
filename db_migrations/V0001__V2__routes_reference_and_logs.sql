CREATE TABLE IF NOT EXISTS routes_reference (
    id SERIAL PRIMARY KEY,
    from_city TEXT NOT NULL,
    to_city TEXT NOT NULL,
    km_normal INTEGER NOT NULL DEFAULT 0,
    km_special INTEGER NOT NULL DEFAULT 0,
    notes TEXT,
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW(),
    UNIQUE(from_city, to_city)
);

CREATE TABLE IF NOT EXISTS route_calculations_log (
    id SERIAL PRIMARY KEY,
    from_city TEXT NOT NULL,
    to_city TEXT NOT NULL,
    stops TEXT[],
    car_class TEXT NOT NULL DEFAULT 'standard',
    km_normal INTEGER NOT NULL,
    km_special INTEGER NOT NULL,
    km_total INTEGER NOT NULL,
    price INTEGER NOT NULL,
    all_prices JSONB,
    reference_id INTEGER REFERENCES routes_reference(id),
    ref_km_total INTEGER,
    deviation_pct NUMERIC(5,1),
    is_error BOOLEAN DEFAULT FALSE,
    error_reason TEXT,
    source TEXT DEFAULT 'osrm',
    created_at TIMESTAMP DEFAULT NOW()
);


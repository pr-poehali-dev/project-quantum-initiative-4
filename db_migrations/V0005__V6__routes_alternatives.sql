-- Альтернативные маршруты: быстрее и дешевле
CREATE TABLE IF NOT EXISTS routes_alternatives (
    id SERIAL PRIMARY KEY,
    from_city TEXT NOT NULL,
    to_city TEXT NOT NULL,
    variant TEXT NOT NULL CHECK (variant IN ('faster', 'cheaper')),
    km_normal INTEGER NOT NULL DEFAULT 0,
    km_special INTEGER NOT NULL DEFAULT 0,
    via TEXT,
    time_hours NUMERIC(4,1),
    notes TEXT,
    UNIQUE(from_city, to_city, variant)
);


def haversine(lat1, lon1, lat2, lon2):
    R = 6371
    d_lat = math.radians(lat2 - lat1)
    d_lon = math.radians(lon2 - lon1)
    a = math.sin(d_lat / 2) ** 2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(d_lon / 2) ** 2
    return R * 2 * math.asin(math.sqrt(a))


def point_in_polygon(lat, lon, poly):
    n = len(poly)
    inside = False
    j = n - 1
    for i in range(n):
        xi, yi = poly[i]
        xj, yj = poly[j]
        if ((yi > lon) != (yj > lon)) and (lat < (xj - xi) * (lon - yi) / (yj - yi) + xi):
            inside = not inside
        j = i
    return inside


def is_in_special_zone(lat, lon):
    return any(point_in_polygon(lat, lon, p) for p in SPECIAL_POLYGONS)


def get_zone_name(lat, lon):
    for idx, poly in enumerate(SPECIAL_POLYGONS):
        if point_in_polygon(lat, lon, poly):
            return ZONE_NAMES[idx]
    return None


def geocode_city(address):
    addr_lower = address.lower().strip()
    for key, coord in CITY_COORDS.items():
        if key == addr_lower:
            return coord
    for key, coord in CITY_COORDS.items():
        if key in addr_lower or addr_lower in key:
            return coord
    api_key = os.environ.get("YANDEX_ROUTES_API_KEY", "")
    if api_key:
        try:
            url = (f"https://geocode-maps.yandex.ru/1.x/"
                   f"?apikey={api_key}&geocode={urllib.request.quote(address)}&format=json&results=1")
            req = urllib.request.Request(url, headers={"User-Agent": "route-builder/1.0"})
            with urllib.request.urlopen(req, timeout=10) as r:
                data = json.loads(r.read())
            members = data["response"]["GeoObjectCollection"]["featureMember"]
            if members:
                pos = members[0]["GeoObject"]["Point"]["pos"].split()
                return (float(pos[1]), float(pos[0]))
        except Exception:
            pass
    try:
        url = (f"https://nominatim.openstreetmap.org/search"
               f"?q={urllib.request.quote(address)}&format=json&limit=1&accept-language=ru")
        req = urllib.request.Request(url, headers={"User-Agent": "route-builder/1.0"})
        with urllib.request.urlopen(req, timeout=10) as r:
            data = json.loads(r.read())
        if data:
            return (float(data[0]["lat"]), float(data[0]["lon"]))
    except Exception:
        pass
    return None


def yandex_route(lat1, lon1, lat2, lon2):
    api_key = os.environ.get("YANDEX_ROUTES_API_KEY", "")
    if not api_key:
        return None
    url = (f"https://api.routing.yandex.net/v2/route"
           f"?waypoints={lon1},{lat1}|{lon2},{lat2}&mode=driving&apikey={api_key}")
    req = urllib.request.Request(url, headers={"User-Agent": "route-builder/1.0"})
    with urllib.request.urlopen(req, timeout=30) as r:
        return json.loads(r.read())


def extract_polyline(route_data):
    points = []
    total_duration = 0.0
    total_length = 0.0
    for leg in route_data.get("route", {}).get("legs", []):
        for step in leg.get("steps", []):
            total_duration += step.get("duration", 0)
            total_length += step.get("length", 0)
            for pt in step.get("polyline", {}).get("points", []):
                points.append((pt[1], pt[0]))
    return points, total_length / 1000.0, total_duration / 3600.0


def split_km_by_zone(waypoints, total_km):
    h_normal = 0.0
    h_special = 0.0
    for i in range(len(waypoints) - 1):
        lat1, lon1 = waypoints[i]
        lat2, lon2 = waypoints[i + 1]
        seg = haversine(lat1, lon1, lat2, lon2)
        mid_lat = (lat1 + lat2) / 2
        mid_lon = (lon1 + lon2) / 2
        if is_in_special_zone(mid_lat, mid_lon):
            h_special += seg
        else:
            h_normal += seg
    h_total = h_normal + h_special
    if h_total == 0:
        return total_km, 0.0
    ratio_special = h_special / h_total
    km_special = total_km * ratio_special
    km_normal = total_km * (1 - ratio_special)
    return round(km_normal), round(km_special)


def find_zone_segments(waypoints):
    segments = []
    current_zone = None
    start_idx = 0
    for i in range(len(waypoints)):
        lat, lon = waypoints[i]
        zone = get_zone_name(lat, lon)
        if zone != current_zone:
            if current_zone is not None:
                segments.append({"start_idx": start_idx, "end_idx": i - 1, "zone": current_zone})
            current_zone = zone
            start_idx = i
    if current_zone is not None:
        segments.append({"start_idx": start_idx, "end_idx": len(waypoints) - 1, "zone": current_zone})
    return segments


def normalize_city(name):
    import re
    n = name.strip()
    parts = [p.strip() for p in n.split(',')]
    skip_words = ['россия', 'область', 'регион', 'район', 'республика', 'округ', 'край']
    city_part = None
    for i, part in enumerate(parts):
        pl = part.lower()
        if re.match(r'^(г|с|п|пгт)\.?\s+', pl) or pl.startswith('город ') or pl.startswith('село '):
            city_part = part.strip()
            break
        if not any(w in pl for w in skip_words):
            city_part = part.strip()
    if city_part:
        n = city_part
    for prefix in ['г. ', 'г.', 'город ', 'с. ', 'с.', 'село ', 'п. ', 'п.', 'пгт. ', 'пгт.', 'пгт ']:
        if n.lower().startswith(prefix):
            n = n[len(prefix):].strip()
            break
    return n.strip().title()



def handle_build_route(from_city, to_city):
    coord_from = geocode_city(from_city)
    if not coord_from:
        return {"statusCode": 400, "headers": CORS, "body": json.dumps({"error": f"Не удалось геокодировать: {from_city}"}, ensure_ascii=False)}
    coord_to = geocode_city(to_city)
    if not coord_to:
        return {"statusCode": 400, "headers": CORS, "body": json.dumps({"error": f"Не удалось геокодировать: {to_city}"}, ensure_ascii=False)}

    lat1, lon1 = coord_from
    lat2, lon2 = coord_to

    route_data = None
    source = "yandex"
    try:
        route_data = yandex_route(lat1, lon1, lat2, lon2)
    except Exception:
        pass

    if route_data:
        waypoints, km_total, duration_hours = extract_polyline(route_data)
        if not waypoints:
            return {"statusCode": 500, "headers": CORS, "body": json.dumps({"error": "Яндекс вернул пустой маршрут"}, ensure_ascii=False)}
    else:
        source = "haversine"
        km_total = haversine(lat1, lon1, lat2, lon2) * 1.3
        duration_hours = km_total / 80.0
        steps = 50
        waypoints = []
        for s in range(steps + 1):
            t = s / steps
            waypoints.append((lat1 + (lat2 - lat1) * t, lon1 + (lon2 - lon1) * t))

    km_normal, km_special = split_km_by_zone(waypoints, km_total)
    zone_segments = find_zone_segments(waypoints)

    polyline_out = [[round(lat, 5), round(lon, 5)] for lat, lon in waypoints]
    if len(polyline_out) > 1000:
        step = len(polyline_out) // 1000
        polyline_out = polyline_out[::step] + [polyline_out[-1]]

    result = {
        "from": from_city,
        "to": to_city,
        "km_normal": km_normal,
        "km_special": km_special,
        "km_total": km_normal + km_special,
        "duration_hours": round(duration_hours, 1),
        "source": source,
        "polyline": polyline_out,
        "zone_segments": zone_segments,
    }
    return {"statusCode": 200, "headers": CORS, "body": json.dumps(result, ensure_ascii=False)}


def handle_update_route(body):
    from_city = body.get("from", "")
    to_city = body.get("to", "")
    route_id = body.get("route_id")

    conn = get_db()
    cur = conn.cursor()

    if route_id and not (from_city and to_city):
        cur.execute("SELECT from_city, to_city FROM routes_reference WHERE id = %s", (route_id,))
        row = cur.fetchone()
        if not row:
            conn.close()
            return {"statusCode": 404, "headers": CORS, "body": json.dumps({"error": "Маршрут не найден"}, ensure_ascii=False)}
        from_city, to_city = row[0], row[1]

    if not from_city or not to_city:
        conn.close()
        return {"statusCode": 400, "headers": CORS, "body": json.dumps({"error": "Укажите from и to или route_id"}, ensure_ascii=False)}

    coord_from = geocode_city(from_city)
    coord_to = geocode_city(to_city)
    if not coord_from or not coord_to:
        conn.close()
        return {"statusCode": 400, "headers": CORS, "body": json.dumps({"error": "Не удалось геокодировать города"}, ensure_ascii=False)}

    lat1, lon1 = coord_from
    lat2, lon2 = coord_to

    route_data = yandex_route(lat1, lon1, lat2, lon2)
    if not route_data:
        conn.close()
        return {"statusCode": 500, "headers": CORS, "body": json.dumps({"error": "Яндекс Routes API недоступен"}, ensure_ascii=False)}

    waypoints, km_total, duration_hours = extract_polyline(route_data)
    if not waypoints:
        conn.close()
        return {"statusCode": 500, "headers": CORS, "body": json.dumps({"error": "Яндекс вернул пустой маршрут"}, ensure_ascii=False)}

    km_normal, km_special = split_km_by_zone(waypoints, km_total)

    from_norm = normalize_city(from_city)
    to_norm = normalize_city(to_city)

    cur.execute(
        "SELECT id FROM routes_reference "
        "WHERE LOWER(from_city) = LOWER(%s) AND LOWER(to_city) = LOWER(%s) LIMIT 1",
        (from_norm, to_norm)
    )
    existing = cur.fetchone()
    if existing:
        cur.execute(
            "UPDATE routes_reference SET km_normal = %s, km_special = %s, "
            "notes = 'yandex-routes', updated_at = NOW() WHERE id = %s",
            (km_normal, km_special, existing[0])
        )
    else:
        cur.execute(
            "INSERT INTO routes_reference (from_city, to_city, km_normal, km_special, notes) "
            "VALUES (%s, %s, %s, %s, 'yandex-routes')",
            (from_norm, to_norm, km_normal, km_special)
        )

    cur.execute(
        "SELECT id FROM routes_reference "
        "WHERE LOWER(from_city) = LOWER(%s) AND LOWER(to_city) = LOWER(%s) LIMIT 1",
        (to_norm, from_norm)
    )
    reverse = cur.fetchone()
    if reverse:
        cur.execute(
            "UPDATE routes_reference SET km_normal = %s, km_special = %s, "
            "notes = 'yandex-routes', updated_at = NOW() WHERE id = %s",
            (km_normal, km_special, reverse[0])
        )

    conn.commit()
    conn.close()

    result = {
        "status": "ok",
        "from": from_norm,
        "to": to_norm,
        "km_normal": km_normal,
        "km_special": km_special,
        "km_total": km_normal + km_special,
        "duration_hours": round(duration_hours, 1),
    }
    return {"statusCode": 200, "headers": CORS, "body": json.dumps(result, ensure_ascii=False)}


def handler(event, context):
    """Построение маршрутов через Яндекс Routes API v2 с разделением километров по зонам повышенного тарифа."""
    if event.get("httpMethod") == "OPTIONS":
        return {"statusCode": 200, "headers": CORS, "body": ""}

    params = event.get("queryStringParameters") or {}
    action = params.get("action", "")

    if action == "zones":
        return handle_zones()

    if action == "build_route":
        from_city = params.get("from", "")
        to_city = params.get("to", "")
        if not from_city or not to_city:
            return {"statusCode": 400, "headers": CORS, "body": json.dumps({"error": "Укажите параметры from и to"}, ensure_ascii=False)}
        return handle_build_route(from_city, to_city)

    if action == "update_route":
        body = {}
        if event.get("body"):
            body = json.loads(event["body"]) if isinstance(event["body"], str) else event["body"]
        return handle_update_route(body)

    return {"statusCode": 400, "headers": CORS, "body": json.dumps({"error": "Неизвестный action. Доступно: zones, build_route, update_route"}, ensure_ascii=False)}
